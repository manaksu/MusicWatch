static void draw_ipod(GContext *ctx) {
  int i;
  graphics_context_set_stroke_color(ctx, GColorDarkGray);
  i = 0;
  while (IPOD_LITE[i] != 255) {
    graphics_draw_line(ctx, GPoint(IPOD_LITE[i+1], IPOD_OY+IPOD_LITE[i]), GPoint(IPOD_LITE[i+2], IPOD_OY+IPOD_LITE[i]));
    i += 3;
  }
  graphics_context_set_stroke_color(ctx, GColorLightGray);
  i = 0;
  while (IPOD_MID[i] != 255) {
    graphics_draw_line(ctx, GPoint(IPOD_MID[i+1], IPOD_OY+IPOD_MID[i]), GPoint(IPOD_MID[i+2], IPOD_OY+IPOD_MID[i]));
    i += 3;
  }
  graphics_context_set_stroke_color(ctx, GColorBlack);
  i = 0;
  while (IPOD_DARK[i] != 255) {
    graphics_draw_line(ctx, GPoint(IPOD_DARK[i+1], IPOD_OY+IPOD_DARK[i]), GPoint(IPOD_DARK[i+2], IPOD_OY+IPOD_DARK[i]));
    i += 3;
  }
}

static void canvas_update_proc(Layer *layer, GContext *ctx) {
  graphics_context_set_fill_color(ctx, GColorWhite);
  graphics_fill_rect(ctx, layer_get_bounds(layer), 0, GCornerNone);

  draw_ipod(ctx);

  time_t now = time(NULL);
  struct tm *t = localtime(&now);
  char time_buf[6];
  char ampm_buf[3];
  int hour = t->tm_hour % 12;
  if (hour == 0) hour = 12;
  snprintf(time_buf, sizeof(time_buf), "%d:%02d", hour, t->tm_min);
  snprintf(ampm_buf, sizeof(ampm_buf), "%s", t->tm_hour < 12 ? "AM" : "PM");

  graphics_context_set_text_color(ctx, GColorBlack);
  graphics_draw_text(ctx, time_buf, s_font_time,
    GRect(32, IPOD_OY + 18, 80, 36),
    GTextOverflowModeWordWrap, GTextAlignmentCenter, NULL);
  graphics_draw_text(ctx, ampm_buf, s_font_ampm,
    GRect(98, IPOD_OY + 20, 20, 14),
    GTextOverflowModeWordWrap, GTextAlignmentLeft, NULL);
}

static void tick_handler(struct tm *tick_time, TimeUnits units_changed) {
  layer_mark_dirty(s_canvas);
}

static void window_load(Window *window) {
  Layer *root = window_get_root_layer(window);
  GRect bounds = layer_get_bounds(root);
  s_font_time = fonts_get_system_font(FONT_KEY_BITHAM_34_MEDIUM_NUMBERS);
  s_font_ampm = fonts_get_system_font(FONT_KEY_GOTHIC_14_BOLD);
  s_canvas = layer_create(bounds);
  layer_set_update_proc(s_canvas, canvas_update_proc);
  layer_add_child(root, s_canvas);
}

static void window_unload(Window *window) {
  layer_destroy(s_canvas);
}

static void init(void) {
  s_window = window_create();
  window_set_window_handlers(s_window, (WindowHandlers){
    .load   = window_load,
    .unload = window_unload,
  });
  window_stack_push(s_window, true);
  tick_timer_service_subscribe(MINUTE_UNIT, tick_handler);
}

static void deinit(void) {
  tick_timer_service_unsubscribe();
  window_destroy(s_window);
}

int main(void) {
  init();
  app_event_loop();
  deinit();
}
